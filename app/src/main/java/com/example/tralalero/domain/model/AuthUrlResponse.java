package com.example.tralalero.domain.model;

public class AuthUrlResponse {
    private String url;
    
    public AuthUrlResponse() {
    }
    
    public AuthUrlResponse(String url) {
        this.url = url;
    }
    
    public String getUrl() {
        return url;
    }
    
    public void setUrl(String url) {
        this.url = url;
    }
}
